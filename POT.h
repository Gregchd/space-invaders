#ifndef POT_H_
#define POT_H_

/****************************************************************************************/
/*****          P R O T O T I P O S   D E    F U N C I O N E S                      *****/
/****************************************************************************************/

/****************************************************************************************/
/*****                      POT_InicializaPD0()                                     *****/
/****************************************************************************************/
/***** Configura el pin PD0 como entrada del ADC0 asociado al canal 7.              *****/
/***** El inicio de conversion es por fimrware.                                     *****/
/****************************************************************************************/
/***** ENTRADAS:    Ninguna                                                         *****/
/***** SALIDA:      Ninguna                                                         *****/
/****************************************************************************************/
void POT_InicializaPD0(void);

/****************************************************************************************/
/*****                      		POT_LeePD0()                                    *****/
/****************************************************************************************/
/***** Realiza la lectura de voltaje por el pin PD0 y retorna un valor escalado     *****/
/***** de salida digital entre 0 a 4095 con entrada entre 0 o 3.3V.                 *****/
/****************************************************************************************/
/***** ENTRADAS:    Ninguna                                                         *****/
/***** SALIDA:                                                                      *****/
/*****    uint16_t:   Lectura del ADC 4095 si a la entrada se tiene 3.3V,           *****/
/*****                0 si a la entrada se tiene 0V.                                *****/
/****************************************************************************************/
uint16_t POT_LeePD0(void);

#endif /* POT_H_ */

